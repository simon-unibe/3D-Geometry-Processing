void CurveSmoothingPlugin::slotImportOVM()
{
    // Note: You may ignore this function, it's not really related to the point of the exercise.

    for (BaseObjectData * obj: PluginFunctions::objects(
                PluginFunctions::TARGET_OBJECTS,
                DATA_POLYHEDRAL_MESH))
    {
        PolyhedralMeshObject *pmo = PluginFunctions::polyhedralMeshObject(obj);
        PolyhedralMesh const &mesh = *pmo->mesh();
        auto visited = mesh.create_private_vertex_property("visited", false);

        auto line = PolyLine{false};
        int count = 0;
        // simple but inefficient hack to ensure we start enumerating each polyline at
        // one of its end vertices: sort vertices by their valence.
        // This works as long as the input is a collection of polylines,
        // and may or may not do something sensible if we have vertices with valence >=3.
        std::vector<std::pair<int, OpenVolumeMesh::VH>> valence_vh;
        valence_vh.reserve(mesh.n_vertices());
        for (const auto vh: mesh.vertices()) {
            valence_vh.emplace_back(mesh.valence(vh), vh);
        }
        std::sort(valence_vh.begin(), valence_vh.end());

        // Run depth-first search to turn OVM mesh into collection of polylines
        for (const auto& [valence, first_vh] : valence_vh) {
            line.set_closed(false); // we will close it if necessary
            line.clear();
            if (visited[first_vh]) continue;
            auto cur_vh = first_vh;
            auto last_vh = OpenVolumeMesh::VH(-1);

            bool keep_going = true;
            do {
                line.add_point(mesh.vertex(cur_vh));
                visited[cur_vh] = true;
                keep_going = false;
                for (const auto next_vh: mesh.vertex_vertices(cur_vh)) {
                    if (next_vh == last_vh) continue;
                    if (next_vh == first_vh) {
                        line.set_closed(true);
                        break;
                    }
                    if (visited[next_vh]) continue;
                    last_vh = cur_vh;
                    cur_vh = next_vh;
                    keep_going = true;
                    break;
                }
            } while(keep_going);

            if ( (!line.is_closed() && line.n_vertices() < 2)
                || (line.is_closed() && line.n_vertices() < 3))
            {
                continue;
            }

            int obj_id = BaseObject::NOOBJECT;
            auto& curve_obj = getOrCreateCurveObject(obj_id,
                    QString("Imported curve %1").arg(++count),
                    ACG::Vec4f(.5f, .4f, 1.f, 1.f));
            curve_obj.target(true);
            *curve_obj.line() = line;
        }
    }
}

void CurveSmoothingPlugin::slotCreateRandomCurve()
{
    // Note: You may ignore this function, it's not super exciting.
    //       feel free to substitute your own random (or less random)
    //       curve generation. Let us know if you come up with something nicer than this.

    auto &input_curve_obj = getOrCreateInputCurveObject();
    auto &polyline = *input_curve_obj.line();
    polyline.clear();
    polyline.set_closed(tool_->cb_closed->isChecked());

    const bool two_d = tool_->cb_curve2d->isChecked();
    const int n_points = tool_->sb_npoints->value();

    int seed = tool_->sb_seed->value();
    std::default_random_engine gen(seed);
    std::normal_distribution<double> noise_dist(0., 1. / (n_points+3));
    std::uniform_real_distribution<double> phase_dist(0., 2*M_PI);

    constexpr int n_octaves = 10;
    std::array<double, n_octaves> phases;
    for (int i = 0; i < n_octaves; ++i) {
        phases[i] = phase_dist(gen);
    }
    for (int k = 0; k < n_points; ++k) {
        const double phi = 2 * M_PI * (k + .5) / n_points;
        Point p{0.};
        double freq = 1.;
        for (int i = 1; i <= n_octaves; ++i) {
            auto w = 1. / (i*i);
            auto phase = phases[i-1];
            p[0] += w * sin(freq * phi + phase);
            p[1] += w * cos(freq * phi + phase);
            if (!two_d) { p[2] += w * sin(2 * freq * phi + phase); }
            freq *= 2;
        }
        if (0) {
            p[0] += noise_dist(gen);
            p[1] += noise_dist(gen);
            if (!two_d) { p[2] += noise_dist(gen); }
        }
        polyline.add_point(p);
    }
    slotSmooth();
    emit updatedObject(input_curve_obj.id(), UPDATE_ALL);
}


void CurveSmoothingPlugin::smoothCurve(
        PolyLine input, PolyhedralMesh *out_mesh, PolyhedralMesh *vis_mesh)
{
    const int n_steps = tool_->sb_steps->value();
    CurveSmoothingMethod method;
    if (tool_->rb_laplacian->isChecked()) {
        method = CurveSmoothingMethod::UniformLaplacian;
    } else {
        assert(tool_->rb_osc_circle->isChecked());
        method = CurveSmoothingMethod::OsculatingCircle;
    }
    const double eps = tool_->dsb_eps->value();

    /// Copy a given polyline into a OVM mesh, return vector of edge handles
    auto add_polyline_to_ovm = [](PolyhedralMesh &mesh, PolyLine const& line)
        -> std::vector<OpenVolumeMesh::EdgeHandle>
    {
        std::vector<OpenVolumeMesh::EdgeHandle> edges; edges.reserve(line.n_edges());
        int first_new_vh = mesh.n_vertices();
        auto last_vh = OpenVolumeMesh::VertexHandle(-1);
        for (auto &p: line.points()) {
            auto vh = mesh.add_vertex(p);
            if (last_vh.is_valid()) {
                auto eh = mesh.add_edge(last_vh, vh);
                edges.push_back(eh);
            }
            last_vh = vh;
        }
        if (line.is_closed()) {
            auto eh = mesh.add_edge(last_vh, OpenVolumeMesh::VertexHandle(first_new_vh));
            edges.push_back(eh);
        }
        return edges;
    };

    /// helper for visualizing smoothing trajectories
    auto add_quad_strip_to_ovm = [] (
        PolyhedralMesh &mesh,
        std::vector<OpenVolumeMesh::EdgeHandle> const &last,
        std::vector<OpenVolumeMesh::EdgeHandle> const &cur)
    {
        assert(cur.size() == last.size());
        int n = last.size();
        for (int i = 0; i < n; ++i) {
            mesh.add_face({
                mesh.edge(last[i]).from_vertex(),
                mesh.edge(last[i]).to_vertex(),
                mesh.edge(cur[i]).to_vertex(),
                mesh.edge(cur[i]).from_vertex()
                });
        }
    };
    std::vector<OpenVolumeMesh::EdgeHandle> ovm_edges;

    PolyLine output;
    output.set_closed(input.is_closed());
    output.resize(input.n_vertices());
    if (vis_mesh) {
        ovm_edges = add_polyline_to_ovm(*vis_mesh, input);
    }
    double totalLength = polylineLength(input.points(), input.is_closed());
    for (int i = 0; i < n_steps; ++i) {
        switch (method) {
            case CurveSmoothingMethod::UniformLaplacian:
                smoothLaplacian(input.points(), output.points(), input.is_closed(), eps);
                break;
            case CurveSmoothingMethod::OsculatingCircle:
                smoothCurvatureFlow(input.points(), output.points(), input.is_closed(), eps);
                break;
        }

        if (tool_->cb_rescale->isChecked()) {
            rescaleCurve(output.points(), input.is_closed(), totalLength);
        }

        if (vis_mesh) {
            auto edges = add_polyline_to_ovm(*vis_mesh, output);
            if (!ovm_edges.empty()) {
                add_quad_strip_to_ovm(*vis_mesh, ovm_edges, edges);
            }
            ovm_edges = edges;
        }
        // Use output as input for the next iteration:
        std::swap(input, output);
    }
    if (n_steps > 0) {
        // The last iteration has swapped input and output, so "input" contains the final result:
        add_polyline_to_ovm(*out_mesh, input);
    }
}

void CurveSmoothingPlugin::slotSmooth()
{

    PolyhedralMesh *vis_mesh = nullptr;
    if (tool_->cb_vis_intermediate->isChecked()) {
        auto &pmo = getOrCreateVisualizationMeshObject();
        vis_mesh = pmo.mesh();
        vis_mesh->clear(false);
    } else {
        auto *pmo = PluginFunctions::polyhedralMeshObject(vis_mesh_obj_id_);
        if (pmo) pmo->hide();
    }

    auto &output_obj = getOrCreateOutputMeshObject();
    output_obj.mesh()->clear(false);


    for (BaseObjectData * obj: PluginFunctions::objects(
                PluginFunctions::TARGET_OBJECTS,
                DATA_POLY_LINE))
    {
        auto *input = PluginFunctions::polyLineObject(obj->id())->line();
        if (input->n_vertices() < 2) { return; }
        smoothCurve(*input, output_obj.mesh(), vis_mesh);
    }

    emit updatedObject(output_obj.id(), UPDATE_ALL);
    if (vis_mesh) {
        emit updatedObject(vis_mesh_obj_id_, UPDATE_ALL);
    }

}

