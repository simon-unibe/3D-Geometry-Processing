double polylineMinEdgeLength(SimplePolyLine const& line, bool is_closed)
{
    double shortest_edge = std::numeric_limits<double>::infinity();
    // TODO: compute length of shortest edge

    for (size_t i = 0; i < line.size() - 1; i++)
    {
        double len = (line[i] - line[i+1]).norm();
        if (len < shortest_edge)
            shortest_edge = len;
    }
    if (is_closed)
    {
        double len = (line.front() - line.back()).norm();
        if (len < shortest_edge)
            shortest_edge = len;
    }

    return shortest_edge;
}

