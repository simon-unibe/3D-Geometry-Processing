rescaleCurve(SimplePolyLine &line, bool closed, double target_length)
{
    // TODO: implement rescaling: modify `line` to have
    // target_length, while keeping the same center.
    //
    // Hint: use polylineLength() and polylineCenter()

    double      scaling_factor = target_length / polylineLength(line, closed);
    const auto& COM            = polylineCenter(line, closed);

    for (auto& v : line)
        v = COM + (v - COM) * scaling_factor;
}

void
CurveSmoothingPlugin::
smoothLaplacian(SimplePolyLine const& input, SimplePolyLine &output, bool closed, double eps)
{
    assert (&input != &output);
    int n = input.size();
    for (int i = 0; i < n; ++i) {
        // TODO: Implement uniform Laplacian smoothing:
        //       For each vertex (except for endpoints, if the curve is not closed):
        //          Compute the vector from the vertex towards the average position of its neighbors
        //          Scale this vector by eps.
        //          Move the vertex input[i] along the scaled vector, store the result in output[i]
        //

        if (!closed && (i == 0 || i == n-1)) 
        {
            output[i] = input[i];
            continue;
        }

        const auto& midpoint = (input[(n+i-1)%n] + input[(i+1)%n]) / 2.0;

        output[i] = (1.0 - eps) * input[i] + eps * midpoint;
    }
}


Point compute_circumcenter(const Point& p0, const Point& p1, const Point& p2) {

    // TODO: implement this helper function.
    //       If the points are (approximately) collinear,
    //       it's convenient to just return p1.

    const double D = (p0 - p1).cross(p1 - p2).norm();

    if (D < 1e-10) return p1;
    
    const double alpha = (p1 - p2).dot(p1 - p2) * (p0 - p1).dot(p0 - p2) / (2.0 * D * D);
    const double beta  = (p0 - p2).dot(p0 - p2) * (p1 - p0).dot(p1 - p2) / (2.0 * D * D);
    const double gamma = (p0 - p1).dot(p0 - p1) * (p2 - p0).dot(p2 - p1) / (2.0 * D * D);

    return (alpha * p0 + beta * p1 + gamma * p2);

}

void
CurveSmoothingPlugin::
smoothCurvatureFlow(SimplePolyLine const& input, SimplePolyLine &output, bool closed, double eps)
{
    using Point = ACG::Vec3d;
    const int n = input.size();
    double global_l_min = polylineMinEdgeLength(input, closed);
    double global_dt = (global_l_min * global_l_min);
    //double global_dt = 1.0; // Q: What happens if you use this? Can you choose a good step size?

    for (int i = 0; i < n; ++i) {
        // TODO: Implement curve-shortening flow using (1D mean curvature flow)
        //       For each vertex v (except for endpoints, if the curve is not closed):
        //          - Compute the center c of the discrete osculating circle
        //          - Scale the vector c-v to have the discrete curvature k=1/R as length (Menger curvature)
        //          - move each vertex along this vector, scaled by eps.
        //          - For stability, scale the step with global_dt = l_min^2.
        //              - (You can also experiment with local scaling based on the
        //                 minimum length of incident edges, this has pros and cons)
        //
        //
        if (!closed && (i == 0 || i == n-1)) 
        {
            output[i] = input[i];
            continue;
        }

        const auto&  c = compute_circumcenter(input[(n+i-1)%n], input[i], input[(i+1)%n]);
        const auto&  v = (c - input[i]);
        const auto&  n = v.normalized();
        const double k = 1.0 / (v).norm();

        output[i] = input[i] + eps * global_dt * k * n;
    }
}


